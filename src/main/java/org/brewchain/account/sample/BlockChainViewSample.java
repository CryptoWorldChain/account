	private BlockView getNode(String hash, BlockView node) {
		BlockView findNode = null;
		if (node == null) {
			return null;
		}
		if (node.hash.equals(hash)) {
			return node;
		} else {
			for (int i = 0; i < node.children.size(); i++) {
				findNode = getNode(hash, node.children.get(i));
				if (findNode != null) {
					return findNode;
				}
			}
		}
		return findNode;
	}

